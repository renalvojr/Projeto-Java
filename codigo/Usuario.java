public class Usuario {
    
    // Atributos
    String nome;
    String tipo;
    int projeto;
    int atividade;

     // objeto
     Usuario(String nome, String tipo, int projeto, int atividade) {
        this.nome = nome;
        this.tipo = tipo;
        this.projeto = projeto;
        this.atividade = atividade;
    }
}